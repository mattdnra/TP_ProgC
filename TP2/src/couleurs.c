#include <stdio.h>

struct Couleur {
    unsigned char r, g, b, a;
};

int main() {
    struct Couleur c[10] = {
        {0xef,0x78,0x12,0xff},
        {0x2c,0xc8,0x64,0xff},
        {0x10,0x20,0x30,0xaa},
        {0xff,0x00,0x00,0xff},
        {0x00,0xff,0x00,0xff},
        {0x00,0x00,0xff,0xff},
        {0x12,0x34,0x56,0x78},
        {0xab,0xcd,0xef,0x55},
        {0x88,0x99,0xaa,0xbb},
        {0x01,0x02,0x03,0x04}
    };

    for (int i = 0; i < 10; i++) {
        printf("Couleur %d:\n", i+1);
        printf("R : %d\n", c[i].r);
        printf("G : %d\n", c[i].g);
        printf("B : %d\n", c[i].b);
        printf("A : %d\n\n", c[i].a);
    }
    return 0;
}
